<h2>The Edited Script </h2>

<p>After the section header you will find the edited text of the unit in the form of a
    performance script. (A longer discussion of the editorial principles used to create
    this script appears in the resource section following the play.) </p>
<p>It is useful to be certain that you are viewing the script in landscape mode of
    tablets or phones, and the size of the type is adjusted so that a line of verse
    appears unbroken while occupying as much of the page as possible. On a laptop or
    desktop this will happen relatively automatically.</p>
<p>You may note that the directly performable words of the play are rendered in serif type, like this:</p>
<div class="example-text">
<ol><li>Shakespeare's words, to be spoken onstage are in this typeface.</li></ol>
</div>
<p>All other aspects of the play, like stage directions, speech headings, and section headers,
(as well as the editorial essays) appear in this san serif font.</p>
<p>The appearance of the section depends on the mode in which it is being viewed.
    Discussions of the four modes appear in the sections that follow.</p>
