<p>Note: The play contains characters called “Fairy” in Unit 7, and “First and Second
    Fairies” in Unit 12, as well as a non-speaking fairy identified as the “Sentinel” in
    Units 12/13, but there is no reason to think that these are not meant to be played
    by the same actors that play the named fairies that are already onstage. They are
    not true doublings, as much as they are simply open assignments. (That is to say,
    the play does not call for a minimum of eight fairies. Four fairies can do all the
    jobs.)</p>
<h2>Summary:</h2>
<p>Without any real issue beyond thinking through the easily-solved transition from Unit
    33 to 34, the twenty-five identified characters in the cast list can be played by
    just 13 speaking actors using the above doublings. (Non-speaking extras are
    suggested at several points in the play as courtiers to Theseus and as the followers
    and attendants of Oberon and Tytania, but these are not strictly necessary and the
    play is more often produced without them that with them in the modern theatre.)</p>
