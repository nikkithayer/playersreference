<h2>More Theatrical Choices</h2>
<p>In his wonderful book-length discussion of doubling in Shakespeare, scholar Brett
    Gamboa suggests that if one is willing to be just a bit more theatrically daring, it
    is possible for both Hermia and Helena to also triple one of the mechanical/fairy
    pairings. This is possible if the production uses body doubles in the last scene of
    the play, as they both (conveniently) do not speak after the point that they take
    their places in the gallery to watch the mechanicals perform <em>Pyramus and
        Thisbe.</em> This would bring the speaking cast down to 11, although it probably
    still requires at least two extras.</p>
<p>It is not easily possible to go further than that limit without adopting more overt
    theatrical conventions (such as actors playing two parts simultaneously) and/or
    intervening in the text much further, but it is worth noting that it is only because
    they have interjections in between scenes of <em>Pyramus and Thisbe</em> that
    Lysander and Demetrius cannot also triple mechanicals. Some modern productions have
    them do so anyway by reassigning their lines or resorting to more liberties 
    with the traditional staging and text, bringing the speaking cast down to nine.</p>
<h3>At its most extreme then, a creative casting might look like this: </h3>
<ul>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Theseus/Oberon } played by one actor</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Hippolyta/Tytania } played by one actor</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Philostrate/Puck } played by one actor</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Egeus/Quince } played by one actor</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Hermia/Snug/Moth } played by one actor</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Lysander/Starveling/Cobweb } played by one
        actor</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Demetrius/Snout/Mustardseed } played by one
        actor</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Helena/Flute/Peaseblossom } played by one
        actor</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Bottom } played by one actor</li>
</ul>
<p></p>
<p>Of course, a small ensemble is not always the goal. For amateur and school
    productions, it is often useful to have a large number of roles available and it is
    not necessarily desirable to use doublings with less experienced actors.
    Professional productions, however, usually want to do so. Although it is
    economically useful to employ a smaller number of speaking actors, the better reason
    is that the play is theatrically more interesting with virtuosic displays of acting
    as a result of doubling and tripling. Further the repetition of themes of the play
    can be highlighted by thoughtful employment of actors in related roles. </p>
<p>Much about the play suggests that this was, in keeping with Elizabethan theatrical
    custom generally, the original practice. Modern performance history tells us that
    the play is now more successful and popular when performed in this manner, whether
    or not it was originally played this way.</p>