<h1> Doubling in <em>Midsummer</em>
</h1>
<p><em>A Midsummer Night’s Dream</em> is a play that seems designed for extensive
        <strong>doubling</strong> by actors – that is, actors playing more than one role
    in the play. A quick glance at the Act-Scene-Character breakdown (see Appendix 1)
    reveals clear patterns of character groups that disappear from the play for
    stretches of time, while another group of characters of almost identical
    configuration replaces them. These two groups may alternate scenes throughout the
    play, but never appear onstage together. Identification of these groupings, and how
    they alternate follows:</p>
<h2>1. Athenians/Supernaturals</h2>
<p>Theseus, Hippolyta and Philostrate always appear together. They are conspicuously
    absent during the times their supernatural counterparts – Oberon, Tytania and Puck –
    hold the stage. Since Peter Brook’s groundbreaking production in 1969, it has become
    almost traditional for Theseus/Oberon to be played by a single actor,
    Hippolyta/Tytania to be played by a single actor, and Philostrate/Puck to double.
    These pairings are both theatrically entertaining and thematically resonant. (A
    number of strict traditionalists insist that this could not have been Shakespeare’s
    original intention because Oberon and Tytania exit at the end of Unit 33 while
    Theseus and Hippolyta begin Unit 34. At least on paper, this seems an unmanageable
    costume change and reëntrance. A half century of theatrical productions have devised
    so many possible solutions for this problem, ranging from ingenious quick changes,
    to insertion of a short musical interlude, to reversing the order of Units 34 and
    35, to making the change onstage in a highly theatrical manner, that this objection
    now seems pedantic and quaint.)</p>
<h2>2. Egeus/Mechanical</h2>
<p>As the lines are assigned in the quartos, Egeus appears only in the first scene and
    again briefly in the fourth act, but disappears conveniently from the play before
    the final act. This means that the actor playing this role is available to double
    one of the mechanicals, usually Quince.</p>
<p>(In the Folio, Egeus is assigned all of Philostrate’s lines from the final act, but
    the fact that this makes this small part essentially unable to double any other
    roles is just one among a number of reasons for being skeptical about the authority
    of this change of line assignments in the Folio.) </p>
<h2>3. Mechanicals/Fairies</h2>
<p>Bottom’s four companions (Flute, Snug, Snout and Starveling) are never on stage with
    the four named fairies (Peaseblossom, Cobweb, Mustardseed and Moth). The only reason
    that this is not an obvious doubling is that centuries of anachronistic practice
    have conditioned a modern audience to think of the fairies as female, and as
    children. This was not an Elizabethan idea, however. The same masque for which Inigo
    Jones designed an evocative Oberon costume (which can be seen in the List of
    Characters, above) also features a set of designs for fairies, which are quite
    clearly adult men. Although this masque is a separate theatrical work than
        <em>Midsummer,</em> it still shows that as late as a couple decades after
        <em>Midsummer</em> premiered “fairies” were still thought of as something like
    "goblins," not female children. This doubling, too, has now become rather common in
    twenty-first century productions.</p>
