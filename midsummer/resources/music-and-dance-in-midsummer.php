<p>The lyrics in the printed text includes two quatrains, followed by two sentences that
    are usually interpreted as spoken dialogue, but which Long thinks was a refrain. The
    ditty is a just a device for Bottom to calm himself, and the lyrics are not
    particularly important or relevant to the story. (The song is about birds, ending
    with the cuckoo, references to which figure in many Elizabethan jokes and folksongs
    for its similarity to “cuckold.”) The lyrics easily fit tunes from a large number of
    Elizabethan ballads and folksongs, as it is written in a simple meter. It is, in
    fact, an example of the song meter (as opposed to the sonnet form) of
    “eight-and-six.”</p>
