<p>Without the extensive illusionistic scenery of the modern theatre, music was also
    important to scene setting. Trumpet fanfares like those used in the actual royal
    palaces announced the entrance of noble characters onto the stage, quickly
    establishing the location when no scenic clues were available to do so. Hunting
    horns might signal a shift to an outdoor setting because of their association with
    the aristocracy at their favorite form of play. Instrumental consorts playing
    background music could establish social gatherings like Capulet’s ball or Timon’s
    dinner party. It might even subtly imply scenes are set at night and in the dark,
    when the theatre was, in reality, brilliantly illuminated in the afternoon sun. In a
    theatrical tradition much more focused on hearing than seeing, music could convey
    dramatic information as well as entertain.</p>
<p>References to dance are metaphorically invoked throughout the Shakespeare canon to
    imply harmony and concord, but especially in the comedies, actual dancing is also
    used to symbolize (as it was at court) the reaching of agreement or the resolution
    of discord. Is his study, <em>Shakespeare and the Dance</em>, Alan Brissenden notes,
    “In some plays, <em>Romeo and Juliet, A Midsummer Night’s Dream</em> and <em>The
        Winter’s Tale</em> among them, dance is essential to the dramatic action…”
    Character is developed and plot is conveyed through onstage dancing. The story
    becomes incomprehensible if it is eliminated.</p>
<h2><strong>The Use of Song and Dance in the Play</strong></h2>
<p>Central to <em>MND</em> are three songs, two of which are also dances. The play also
    contains an additional two freestanding dances. All of these play a significant role
    in the story, including the resolution of the major conflict between Oberon and
    Tytania in the supernatural plot.</p>
<p>Beyond these five major music cues, stage directions or dialogue also contain direct
    reference to incidental music on two occasions and specify a trumpet flourish (a
    kind of fanfare) or the sounding of horns on five more occasions. Performance
    practice of the time would suggest several more instances, like the beginning of the
    play for example, where music was probably used but it is not specified in the stage
    directions of the quartos or the folio.</p>
