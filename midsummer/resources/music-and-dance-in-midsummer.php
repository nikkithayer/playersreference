<h1>Music and Dance in <em>A Midsummer Night’s Dream</em></h1>
<h2>The Performing Arts of Elizabethan England.</h2>
<p>Both music and dance were important to country life in early modern England. Folk
    dancing served as the basis for almost all community celebrations, while music
    making was the most common form of entertainment in daily life. Both art forms were
    also central to life at court where they served aesthetic purposes; but were also
    extensively employed symbolically to promote social harmony and signal political
    allegiance to the crown. Both music and dance were widely understood as metaphors
    for cosmic and earthly order.</p>
<p>Both art forms were also integral to the early modern theater. Shakespeare’s theatre,
    especially at the open-air playhouses like the Globe, lacked most means of modern
    day crowd control such as dimmable lighting and assigned seating. Audiences could be
    unruly and loud. Pre-show concerts were common to occupy the time of early arrivers.
    Brass fanfares were used to announce the start of performances because they cut
    through the hubbub in the auditorium, at least temporarily quieting the buzz, and
    focusing the audience’s attention.</p>
