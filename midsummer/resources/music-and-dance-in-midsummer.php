<p>Of greatest importance are the three songs in the play. The first of these happens in
    Unit 12, when Tytania commands her followers to perform a roundel (a kind of round
    dance) and a song. The song has two stanzas in the form of quatrains and a
    seven-phrase refrain that follows both. A two-line coda ordering all the fairies to
    disperse—except a sentinel to keep watch—was originally printed as part of the
    lyrics and it is sometimes sung, but is now usually treated as dialogue.</p>
<p>The refrain of the song is a lullaby that induces the fairy queen to sleep, but the
    quatrains serve a different purpose. They are incantations against the invasion of
    serpents and spiders. The function of the accompanying dance in a circle around
    Tytania is the weaving of a spell that creates a protective perimeter around her. </p>
<p>The structure and tone of the song suggests that it is an art song rather than a folk
    song, which in turn implies that it was accompanied by an instrumental ensemble. (A
    stage direction specifically calls for such an ensemble at a later point in the
    play, and since it was available it is hard to imagine that it was not used here as
    well.)</p>
<p>The early editions are somewhat confusing about the exact assignments of the singers,
    but most settings of the song assign the two stanzas to soloists and the refrain to
    the ensemble. Nothing suggests that specific named fairies must be assigned to any
    particular lyric, however, so there is great flexibility in how the lullaby might be
    performed. In practice, it is common for all the fairies to sing the entire piece in
    unison. It is also fairly common for the stanzas to be spoken or chanted in the
    manner of an incantation over a musical underscoring with only the refrain
    completely sung.</p>
<p>In his study of Shakespeare’s music, John H. Long sets the lyrics to a lullaby found
    in Anthony Holborne’s 1597 collection <em>Pavans, galliards, and other short
        aeirs.</em> Andrew Charlton adapts an anonymous 16th century piece called
    “Barafostus’ Dream” for the same purpose. (See bibliography for how to find these
    realizations.) At this writing a recorded version from Duffin’s <em>Shakespeare’s
