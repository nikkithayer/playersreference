<p>None of the original music has survived. (It is possible, even probable, that
    pre-existing popular tunes were utilized as settings so there was no “original
    music.”) It is easy, however, to determine the function and tone of all the music
    from the words and stage directions alone. For those with historical interest in
    what an Elizabethan performance might have sounded like several sources have
    suggested possible settings from extant tunes, ballads, or airs of the time with
    similar themes and/or meters. A few of these will be discussed below. </p>
<p>This edition identifies 25 probable music cues, and occasions for four dances. In
    Performance Mode these are marked with this symbol: <span class="performer-text">♫</span> 
    Clicking on the symbol will
    open a note that explains briefly the nature and function of the cue. A longer
    discussion of all these items appears in the essay below:</p>
