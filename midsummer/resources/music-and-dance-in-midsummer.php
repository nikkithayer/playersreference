<h2><strong>Lyrical passages</strong></h2>
<p>Many verse passages in the play are extremely lyrical in nature. Although there is no
    indication that these were intended as songs, several of these “purple” passages
    have been set to music often, especially incantations and speeches that are cast in
    iambic tetrameter, sometimes called “magic meter.” The following list gives a sense
    of speeches from the play for which it is easy to find musical settings: </p>
<p>“Love looks not with the eyes” (Unit 5)</p>
<p>“Over hill, over dale” (Unit 7) Gooch and Thatcher list 43 settings</p>
<p>“I know a bank where the wild thyme blows” (Unit 11, line 252) – Gooch and Thatcher
    list 38 settings</p>
<p>“Up and Down.” (Unit 18, line 45)</p>
<p>“Flower of this purple dye” (Unit 23)</p>
<p>“Be as thou are want to be” (Unit 33, line 52)</p>
<p>“Now the hungry lion roars” (Unit 50)</p>
<p>“If we shadows have offended” (Epilogue)</p>
<h2><strong>Annotated Musical Resources</strong></h2>
<p>Charlton, Andrew. <em>Music in the Plays of Shakespeare: A Practicum.</em> Garland,
    1991.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(Charlton’s book is now out of print, but it is well
    worth tracking down for its thorough cue sheets and printed music for suggested
    settings for all the major pieces. The discussion of tone and purpose of each piece
    is very helpful for pre-planning any production whether or not there is any interest
    in historically accurate recreations.)</p>
<p>Duffin, Ross W. <em>Shakespeare's Songbook.</em> Vol. 1, W W Norton, 2014.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(The most recent work providing a discussion of the
    music along with printed possible settings for Shakespeare’s songs, this excellent
    volume is easily available. It has a companion CD carried by Naxos of America, which
    makes it possible to hear recordings of many of the most important musical pieces.
    The recorded music has been made available separately by the publisher on many
    streaming music services, so one can easily listen to most of the examples. The
    greatest virtue of this book is that it discusses many pieces of incidental music in
    addition to the major songs and offers suggestions from the period repertoire that
    might satisfy them. The glaring weakness, at least as concerns <em>Midsummer</em>,
    is that it inexplicably omits any discussion of the final song, “Now Until the Break
    of Day.”)</p>
<p>Gooch, Bryan Niel Shirley, et al. <em>A Shakespeare Music Catalogue.</em> Vol. 2,
    Clarendon Press, 1991.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(This academic catalogue does not provide any music but is simply an exhaustive list
    of known settings. For <em>Midsummer</em> that totals in the thousands. The
    discussion of <em>Midsummer</em> is illuminating as it covers incidental music for
    the theater, settings for songs intended for concert performance, related pieces
    like Britten’s opera, and settings of verse not originally intended as song.
    Although it has not been updated since 1991, it is still exceptionally helpful.)</p>
<p>Long, John H. <em>Shakespeare's Use of Music.</em> Da Capo Press, 1977.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(Now available in an inexpensive reprint by Forgotten Books, Long’s discussion of
    music is sometimes marred by outdated editorial assumptions, but his musical
    selections are useful and his observations about the uses of the music can be very
    astute. He offers very different suggestions of Elizabethan pieces that might work
    from Duffin or Charlton, so this is a good additional resource.)</p>
<p>Place, Gerald, and Rebecca Hickey, singers. Dorothy Linell, Lute. <em>Music for
        Shakespeare's Theatre.</em> Naxos 8.570708, 2008. Musical Recording.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(This recording of period music for Shakespeare’s plays, also made widely available
    by the publisher on streaming services, includes “The Woosel Cock” and “You Spotted
    Snakes,” along with a short version of “Kemp’s Jig.”)</p>
