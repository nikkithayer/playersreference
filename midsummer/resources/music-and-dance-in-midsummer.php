<h3><strong>“Now until the break of day,”</strong> also known as Oberon’s song</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Identified in dialogue as song (and dance) Unit 51,
    Line 348</p>
    <div class="example-text">
    <h4 class="actor">OBERON</h4>
<ol>
<li>Now, until the break of day,</li>
<li>Through this house each fairy stray. </li>
<li>To the best bride-bed will we, </li>
<li>Which by us shall bless&#232;d be,</li>
<li>And the issue there create </li>
<li>Ever shall be fortunate.</li>
<li>So shall all the couples three</li>
<li>Ever true in loving be,</li>
<li>And the blots of Nature's hand </li>
<li>Shall not in their issue stand. </li>
<li>Never mole, harelip, nor scar,</li>
<li>Nor mark prodigious, such as are</li>
<li>Despis&#232;d in nativity,</li>
<li>Shall upon their children be.</li>
<li>With this field-dew consecrate </li>
<li>Every fairy take his gait, </li>
<li>And each several chamber bless,</li>
<li>Through this palace, with sweet peace.</li>
<li>And the owner of it blest,</li>
<li>Ever shall in safety rest.</li>
<li>Trip away. Make no stay.</li>
<li>Meet me all by break of day.</li>
</ol>
</div>
<p>Oberon begins the scene by calling on his followers to give the house “glimmering
    light.” Most modern productions accomplish this by having the fairies enter carrying
    flickering candles, but since in Elizabethan times the carole was a dance performed
    with linked hands, he is more probably (supernaturally) causing or inviting
    twinkling starlight inside the chamber. With modern lighting technology, some
    productions now use small LED lights sewn into the costumes or mounted on
    headdresses to provide this effect and free the actors’ hands.</p>
<p>The form of this masque is six lines of (probably spoken) instruction from Oberon,
    followed by a quatrain extending these instructions by Tytania. At that point, the
    Folio text has a heading that says simply, “The Song.” It is followed by eleven
    couplets which are not specifically assigned to any singer, although the quartos
    omit the heading and assign all of them to Oberon. (Samuel Johnson thought that this
    heading indicated that a (lost) song was inserted here, and that the couplets were a
    spoken section to follow it. The New Oxford Shakespeare seems to accept this
    interpretation. While it is common for song locations to be indicated in this way in
    the Folio, without printing their words, the fact that the couplets were set in
    italic type strongly suggest that Dr. Johnson was mistaken, and that they are lyrics.)</p>
<p>Modern settings utilize a wide variety of assignments of singers to these lyrics.
    While Oberon sometimes sings or intones the whole song, just as often he shares it
    with Tytania and/or with the full ensemble. The couplet “To the best bride-bed will
    we / Which by us will blessed be” seems to indicate plural singers, so it is more than
    defensible to think that all or part of the song should be harmonized (as were most
    caroles) by all the participants.</p>
<p>Ancient caroles, which had something of a processional quality, were generally not
    accompanied but the use of an instrumental consort elsewhere in the play makes it
    plausible that one was also used here.</p>
<p>Andrew Charlton adapts the anonymous 16th century piece, “I Loathe that I Did Love”
    as a setting that gives some sense of what the original might have sounded like.
    (Long prints “The Urchin’s Dance,” written by Edmund Pierce for Thomas Middleton’s
        <em>Blurt, Master Constable</em> as a suggestive analogy, but the tune does not
    fit the meter or form of Oberon’s song, so it serves only to give a feeling of tone
    and style.) Few modern settings attempt to use an Elizabethan style, but most have a
    reverent and harmonious style that provide a fitting conclusion to the play.</p>
<h2><strong>Other Music</strong></h2>
<p><strong>Flourishes and Fanfares</strong></p>
<p>In addition to the essential music cues above, the play calls for short fanfares or
    flourishes, lasting up to ten seconds, to start scenes where Theseus is entering in
    his official role and twice to announce sections of the play-within-the-play,
        <em>Pyramus and Thisbe</em>. Theatrical practice might suggest use of these on
    some other occasions, as well. In the comprehensive list of musical cues (below)
    there are notations about both specified and implied use of trumpets and horns in
    the play. All of these are also notated in Performance Mode. These are among the 25
    cues mentioned at the start of this essay.</p>
<p>Of particular note, however, is the instance of “Sound horns” after Unit 34, line
    120. Because supernatural music has been used to lull the lovers and Bottom into a
    deep sleep, this use of mortal music to wake them and return them to their full
    sensibilities serves as a kind of book-end. Some effort might be expended to relate
    this flourish to the music that “charmeth sleep” in some manner.</p>
<p><strong>Optional incidental music</strong></p>
<p>Because <em>Midsummer</em> is such a musical play, it often involves commissioning a
    complete musical score. For a production utilizing such a score, it is sometimes
    useful to request a few minor pieces of music beyond those specifically called for
    by the text.</p>
<p>In addition to the music noted above, it is common for the mechanicals to have an
    identifying theme that is played at the beginning of their scenes to set the comic
    tone, and the fairies often have their own identifying theme, as well. Notes for
    optional occasions where such music might be used appear in the list of cues below,
    and in Performance Mode.</p>
<p>Bottom calls for music performed on the “tongs and the bones” in Unit 32. These are
    instruments something like the triangle and castanets. Tytania has a line right
    after which seems like a distraction, and the quartos have no musical cue here, but
    the Folio specifies that these instruments actually play. This is an optional
    occasion for a short, perhaps interrupted, piece of music.</p>
<p>Finally, the transition between Units 33 and 34 may require a short musical interlude
    of some sort if Oberon/Theseus and Tytania/Hippolyta are doubling to cover their
    costume change and re-entrance.</p>
<p><strong>One possible incidental dance</strong></p>
<p>In the middle of Unit 18, after Bottom is transformed into an ass, there is an often
    overlooked possible dance cue. As his friends scatter in amazement, Puck enters
    (invisible to them) and says, “I’ll lead you about a round.” Although he may be speaking
    metaphorically, the literal meaning of this line is that he will lead, or “call,” a
    round dance. (In America, these are idiomatically called “square” dances.) One comic
    way of handling this scene is to turn it into a supernaturally compelled dance, and
    it may need both incidental music and choreography.</p>
