<p>In Unit 19, Bottom sings two fragments from what was apparently a popular folksong of
    the period, although the original tune is no longer known. We do not hear the entire
    song because Tytania interrupts his singing. The song is not accompanied and is not
    necessarily intended to be performed well. The comedy of the scene, in fact, comes
    from the contrast between what we hear from the braying of the transformed Bottom
    and what the enchanted Tytania perceives as an “angelic” voice.</p>
