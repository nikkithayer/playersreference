<p><strong>Music such as charmeth sleep</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Identified in dialogue at Unit 33 (IV.i.b), Line 67</p>
<p>The climactic use of music and dance in <em>Midsummer</em> takes place in Unit 33 at
    line 65, when Tytania commands it, or more likely at line 67, when Oberon repeats
    the order. Tytania (speaking to unseen forces) asks for “music, such as charmeth
    sleep.” Oberon then follows up her royal request with “Sound music. Come, my queen,
    take hands with me/ and rock the ground whereon these sleepers be.” His suggestion
    is that they dance in a manner that will “rock” the ground in the same manner that a
    mother would rock a cradle, combining with the music to keep the four lovers (and
    Bottom) in a deep sleep. Oberon and Tytania then dance together, ending their
    enmity, and resolving their quarrel over the changeling boy that has disrupted
    nature.</p>
    <p> Long suggests that there are two separate musical episodes here: First, the
    music that “charmeth sleep” as ordered by Tytania, and once it is completed, a
    separate piece of music to accompany the dance suggested by Oberon. In modern
    production only one piece of music is commonly used, but it sometimes begins when
    Tytania commands it, and then extends when Oberon gets the idea to dance to it as a
    tool of reconciliation.</p>
<p>We know nothing of the original music, but it was certainly supplied by an off-stage
    consort, as it is supposed to be magically induced. It perhaps represents the
    harmonious “music of the spheres” being made audible. The purpose of this music is
    to refresh and restore the lost senses of the mortals after their mentally and
    physically trying night in the woods. We have every reason, then, to think that the
    music might be calm and peaceful. Long proposes John Dowland’s “Sleep, Wayward
    Thoughts” as found in his <em>First Book of Airs</em> (1597) as a good equivalent
    for what might have been played at this point in the original production. Numerous
    recordings of this piece can be easily found, such as at <a
        href="https://www.youtube.com/watch?v=MvlXCe3S52Q" target="_blank">
        <strong><em>"Sleep, Wayward Thoughts"</em></strong></a></p>
