<p><strong>Bergomask</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dance identified in dialogue, occurring at Unit 49
    (V.i.m), line 309</p>
<p>In the early modern period, theatrical events involved more than just the formal
    play. They might include pre-show crowd pleasers like fencing bouts or short
    concerts, and almost always ended with some kind of postlude and a dancing
    demonstration. In keeping with theatrical custom, after performing <em>Pyramus and
        Thisbe</em>, Bottom and his crew offer just such an epilogue or a dance. Already
    familiar with their ability to deliver verse, Theseus wisely chooses the latter. </p>
<p>The dance they propose to perform is a Bergomask, a lively dance that originally
    poked fun at the rustic and unsophisticated folk dances of the Bergamo region of
    Italy. It was perceived then something like tap or clog dancing is now received—
    that is, as something less elevated than art dance like ballet, but also as possibly
    more entertaining and popular.</p>
<p>Bottom’s proposal to the Duke is that two company members will perform this dance,
    but even if only two start it off the Bergomask is almost universally performed by
    the whole group of mechanicals in modern production. As opposed to the ethereal
    dancing of the fairies that has preceded, and will follow, this dance, the Bergomask
    is usually a vigorous, earthy dance. It is not always accompanied by anything but
    clapping and stomping along to the insistent rhythm, but if music is desired almost
    any lively folk or popular dance will serve in this location.</p>
